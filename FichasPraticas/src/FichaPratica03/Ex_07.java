package FichaPratica03;

public class Ex_07 {
    public static void main(String[] args) {

        int counter = 0;

        // Loop to print the integers and their sum
        for (int i = 1; i <= 100; i++) {
            System.out.println(i);

            counter += i;

        }

        System.out.println(counter);
    }
}
